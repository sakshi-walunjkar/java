public class byte_1 {
    public static void main(String[] args) {
        //hexa decimal
        int a=0XBEEF;
        System.out.println(a);

        int b=~98;
        System.out.println(b);

        //octa decimal
        int c=0135;
        System.out.println(c);

        int d=135;
        System.out.println(d);
    }
    
}
